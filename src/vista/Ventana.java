    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String dividendo = jTextField1.getText();
        String divisor = jTextField2.getText();
        /**
         * Se llama a al metodo validador para verificar los String ingresados
         */
        boolean validadorMensaje = metodos.validador(dividendo);
        boolean valiadadorPgenerador = metodos.validador(divisor);
        /**
         * Si el metodo validador regresa true en ambos caso que procede a crear
         * el arreglo en tiempo de ejecucion asi como tambien a ejecutar el
         * metodo para convertir el String en un arreglo numerico para asi poder
         * realizar el calculo de crc
         */
        if (validadorMensaje) {
            if (valiadadorPgenerador) {
                /**
                 * Se habilitan la cajas de texto para el crc y tx se
                 * deshabilant el boton de generar crc, d y g
                 */

                jButton1.setEnabled(false);
                jTextField3.setEnabled(false);
                jTextField3.setDisabledTextColor(Color.black);
                jTextField4.setEnabled(true);
                jButton2.setEnabled(true);
                /**
                 * Se obtiene los tamaños y se manda a llamar al metodo para
                 * crear el vector en tiempo de ejecucion
                 */
                int tamanioMensaje = dividendo.length();
                int tamanioPGenerador = divisor.length();
                metodos.crearVector(tamanioMensaje, tamanioPGenerador);
                /**
                 * Se llama al metodo convertirStringToArray(String mensaje,
                 * String generador) para pasar como parametros la cadena del
                 * mensaje y generador para convertirlos en arreglos de enteros
                 */
                metodos.convertirTextoAArreglo(dividendo, divisor);
                /**
                 * Se llama al metodo combinar arreglo para que genere el
                 * arreglo del mensaje junto al r
                 */
                Integer[] nuevoMensaje = metodos.unirArreglos(metodos.binarioG, metodos.bitsCeros);
                /**
                 * Se llama al metodo algoritmoCRC()para obtener el crc
                 */
                Integer[] resultadoCRC = metodos.calcularCRC(nuevoMensaje);
                /**
                 * El resultado del crc se combina con el mensaje para obtener
                 * el TX
                 */
                Integer[] TX = metodos.unirArreglos(metodos.binarioG, resultadoCRC);

                /**
                 * Se envian a los JtextFiield los datos con los string
                 * correspondientes a el crc y tx
                 */
                jTextField3.setText(metodos.ArrayToString(resultadoCRC));
                jTextField4.setText(metodos.ArrayToString(TX));

            } else {
                JOptionPane.showMessageDialog(null, "El Polinomio generador ingresador tiene un caracter invalido");
            }
        } else {
            JOptionPane.showMessageDialog(null, "El mensaje ingresador tiene un caracter invalido");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        /**
         * Se extrae con la variable tx el texto respetivo se realiza el proceso
         * convertirlo a arreglo y llmamar al metodo algoritmoCRC pasando como
         * parametro el arreglo convertido a Integer
         */
        String tx = jTextField4.getText();
        int txTamanio = tx.length();
        Integer[] txEnteros = metodos.convertirTextoAArreglos(tx);
        Integer[] resultadoCRC = metodos.calcularCRC(txEnteros);
        String dato = metodos.ArrayToString(resultadoCRC);
        int validador = Integer.parseInt(dato);
        /**
         * Se valida si el dato del validador es 0 si es asi manda el mensaje
         * correctos de lo contrario mensaje incorrecto
         */
        if (validador == 0) {
            jLabel7.setText("Mensaje llego correctamente!");
            jTextField6.setText(String.valueOf(validador));
            jTextField5.setText(tx);
        } else {
            jLabel7.setText("Mensaje fue modificado!");
            jTextField6.setText(String.valueOf(validador));
            jTextField5.setText(tx);
          
        }
       
    }//GEN-LAST:event_jButton2ActionPerformed

